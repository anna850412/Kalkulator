package com.kodilla.calculator;

public class Calculator {
    double a = 2;
    double b = 5;

    public Calculator() {
        this.a = a;
        this.b = b;
    }

    public double addAtoB(){
        return a + b;
    }
    public double substractAfromB (double a , double b){
        return a - b;
    }

}
